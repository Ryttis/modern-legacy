        - Today's UNESCO World Heritage site of Kaunas modernist architecture was originally planned in the mid-19th century and developed between 1919 and 1939. The status of the Provisional Capital led to unprecedented urban growth and an abundance of architecture, with twice as many residents.<br>
        - In less than twenty years, thanks to the efforts of the new national government and civic initiatives, Kaunas transformed into a modern city: a place where modern urban planning and architecture blends with existing natural landscapes and urban solutions. Architecture played a particularly crucial role: the architects of the time didn’t simply follow global trends blindly but created unique Lithuanian interpretations of various modernist styles.<br>
        - The UNESCO heritage site consists of two districts: Naujamiestis (the New Town) and Žaliakalnis (the Green Hill). They have the highest concentration of the most significant modernist buildings – 1,500 of the 6,000 still standing: many of which remain largely unchanged, preserving their exterior, interior, details, and in some cases, even their original function.<br>
        Naujamiestis, planned in 1847 with a vast street network, and Žaliakalnis, along with its slopes, saw the rise of new residential districts. These neighbourhoods embraced the highly advanced garden-city concept – providing Kaunas residents with the benefits of urban living amidst natural surroundings, enjoying the health advantages of rural landscapes.
        - The modernist Kaunas, formed between 1919–1939, is a remarkable example of building a modern state, city, and society in the 20th century. This is where the concept of optimism emerges: despite challenging economic, political, and social conditions, people built a modern city, loudly proclaiming their belief in an independent future.<br>
        - The sensitive adaptation of the 19th-century street network, the implementation of the garden-city idea with nature integration, and the local and global interpretations of architectural modernism gave rise to the unique Kaunas modernism. Kaunas modernism was awarded the European Heritage Label, and on September 18, 2023, received the title of UNESCO World Heritage. This recognition signifies that Kaunas represents the entire region, the whole of the new Europe, and the nations that emerged after World War I, constructing new identities. Yet, Kaunas’s identity stands out as the most unique and vivid.
