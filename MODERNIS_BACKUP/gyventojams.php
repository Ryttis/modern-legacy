        <details data-tags="unesco naudinga">
            <summary>Kokia yra Pasaulio paveldo konvencijos reikšmė?</summary>
            <div>UNESCO Pasaulio paveldo konvencija – bene svarbiausia tarptautinė teisinė priemonė, remianti pasaulio kultūros ir gamtos paveldo išsaugojimą. Šiandien 193 valstybės yra prisijungusios prie konvencijos.<br><br>
            Tai viena sėkmingiausių ir veiksmingiausių paveldo apsaugos ir tarptautinio bendradarbiavimo sistemų, vienijančių bendruomenę paveldo išsaugojimui. Vienas pamatinių konvencijos siekių – parodyti atsakingą solidarumą ir bendrą tautų atsakomybę, saugant išskirtines žmonijos vertybes ir kultūrinę patirtį.
            </div>
