        <details data-tags="unesco">
            <summary>Kuo svarbus kultūros ir gamtos vertybių įrašymas į Pasaulio paveldo sąrašą?</summary>
            <div>
                Prie Pasaulio kultūros ir gamtos paveldo apsaugos konvencijos prisijungusi šalis turi teisę įrašyti nacionalines vertybes į Pasaulio paveldo sąrašą. Tai valstybei suteikia galimybę aktyviai dalyvauti tarptautiniuose kultūros paveldo srities bendradarbiavimo procesuose, teikti ir gauti ekspertinę pagalbą meno, mokslo ir technikos srityse, išskirtiniais atvejais – aplikuoti į finansinę paramą.<br><br>
                UNESCO pasaulio paveldo statusas – globaliu mastu galiojantis vardinis ir kokybinis ženklas, reiškiantis tarptautinį pripažinimą. Šios vertybės konkurencingiausios ir lankomiausios turistinės vietovės pasaulyje. Tai vienas iš reikšmingų reprezentacinių resursų prisidedančių prie šalies įvaizdžio formavimo.<br><br>
                Pasaulio paveldo vertybės taip pat svarbus ekonominis šalies išteklius, skatinantis atvykstamąjį turizmą, didinantis šalies patrauklumą, kultūrinį pažinimą, turintis neabejotiną ekonominę grąžą. Tai – vienas iš reikšmingų reprezentacinių resursų prisidedančių prie šalies įvaizdžio formavimo.<br><br>
                Tarptautinis pripažinimas skatina šalies gyventojų pasididžiavimą ir pagarbą nacionalinėmis vertybėms, ugdo visuomenės sąmoningumą, skatina pilietinį solidarumą siekti paveldo išsaugojimo, svarbaus dabarties bei  ateities kartoms. <br><br>
                Didesnis supratimas ir atsakomybė lemia aukštesnį paveldo vertybių apsaugos ir išsaugojimo lygmenį.
            </div>
        </details>
        <details data-tags="unesco">
            <summary>Kaip vertybė įrašoma į sąrašą?</summary>
            <div>
                Prieš pradedant svarstyti vertybės įrašymą į Pasaulio paveldo sąrašą, vyksta ilgas nominavimo procesas, trunkantis bent dvejus metus. Siūlymą įrašyti paveldo objektą gali teikti tik ta šalis, kurioje yra ši vertybė. Prieš pradedant nominavimo procesą vertybė turi patekti į Preliminarųjį konvencijos šalių narių paveldo sąrašą.<br><br>
                Šalis privalo parengti paraišką, kurioje remdamasi Pasaulio paveldo komiteto nustatytais atrankos kriterijais paaiškinami ir išdėstomi argumentai dėl vertybės įrašymo į Pasaulio paveldo sąrašą bei pateikiamas apsaugos ir valdymo priemonės bei planas. Ją teikia oficialios valdžios institucijos. <br><br>
                Parengta paraiška keliauja į Pasaulio paveldo centrą, kur yra peržiūrima ir patikrinama. Tinkami dokumentai perduodami patariamosioms organizacijoms: Tarptautinei gamtos apsaugos sąjungai (IUCN) ir Tarptautinei paminklų ir paveldo vietovių tarybai (ICOMOS). Jos atlieka vertinimą ir pateikia ataskaitą Pasaulio paveldo komitetui. <br><br>
                Pastarasis priima galutinį sprendimą dėl vertybės įrašymo į Pasaulio paveldo sąrašą. 
            </div>
        </details>
        <details data-tags="unesco">
            <summary>Ar vertybė gali būti išbraukta iš sąrašo?</summary>
            <div>
                Tik labai kraštutiniais atvejais ir tik išbandžius visas apsaugos bei išsaugojimo priemones,  vertybė yra išbraukiama iš Pasaulio paveldo sąrašo. <br><br>
                Nuo 1975 metų, tai nutiko tik tris kartus: <br><br>
                <ul>
                    <li>2007 m. – Arabų oriksų buveinė Omane</li>
                    <li>2009 m. – Dresdeno Elbės slėnis Vokietijoje</li>
                    <li>2021 m. – Liverpulis</li>
                </ul><br>
