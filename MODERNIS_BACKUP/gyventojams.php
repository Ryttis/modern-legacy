            <summary>Kas yra UNESCO pasaulio paveldas?</summary>
            <div>
                Pasaulio paveldo sąrašą (angl. World Heritage List) sudaro visai žmonijai reikšmingos ir išskirtinę visuotinę vertę turinčios kultūros ir gamtos paveldo vertybės. Pavyzdžiui, tai Egipto piramidės, Didysis barjerinis rifas Australijoje, Galapagų salos Ekvadore, Tadžmahalas Indijoje, Didysis kanjonas JAV, Akropolis Graikijoje, o nuo šiol ir modernistinis Kaunas. Jų išsaugojimas tampa ne tik šalies, bet ir visos tarptautinės bendruomenės atsakomybė. <br><Br>
                UNESCO pasaulio paveldo sąraše – beveik 1,2 tūkst. gamtos ir kultūros vertybių, esančių 168 valstybių teritorijose. Į Pasaulio paveldo sąrašą įrašytos penkios Lietuvos materialaus paveldo vertybės: <br><br>
                <ul>
                    <li>Vilniaus istorinis centras (1994)</li>
                    <li>Kuršių nerija (2000)</li>
                    <li>Kernavės archeologinė vietovė (2004)</li>
                    <li>Struvės geodezinis lankas (2005)</li>
                    <li>Modernistinis Kaunas (2023)</li>
                </ul>
