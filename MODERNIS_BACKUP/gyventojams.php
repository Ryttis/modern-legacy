            <div>
            Europos paveldo ženklas (ang. European Heritage Label, EHL) – visiškai atskira nuo UNESCO iniciatyva, įgyvendinama Europos komisijos. Ji skirta europietiškai tapatybei stiprinti. <br><br>
            Skirtingai nuo UNESCO pasaulio paveldo sąrašo, Europos paveldo ženklui Kaunas nominavo ne urbanistinę vietovę, o 44 išskirtiniausius modernistinius pastatus. Jų fasadai pažymėti stilizuotomis informacinėmis lentelėmis su EHL logotipu. <br><br>
            Europos paveldo ženklas modernistiniam Kaunui suteiktas 2015 metais. „Kaunas 1919–1940“ – kol kas vienintelė paveldo vertybė Lietuvoje, gavusi šį statusą.  
