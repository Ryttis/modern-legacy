        <span>Modernistinis Kaunas įrašytas į UNESCO pasaulio paveldo sąrašą pagal IV kriterijų. Modernistinis Kaunas  yra išskirtinis istorinio miesto centro, patyrusio sparčią urbanizaciją ir modernizaciją būnant laikinąja sostine (1919–1939), pavyzdys, atspindintis skirtingas vietos gyventojų vertybes ir siekius kurti modernų miestą, vedamus pokario optimistinio tikėjimo nepriklausoma ateitimi neramioje XX a. pradžios Europoje, kuomet vyko valstybių sienų kaita. <br><br>

