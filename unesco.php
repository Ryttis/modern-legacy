                    <span>Tarpukariu Žaliakalnis tapo antru sparčiausiai augančiu miesto rajonu po Naujamiesčio. Kauno burmistras Jonas Vileišis iš Kopenhagos pakvietė garsų urbanistą Marių Frandseną. Jis su tuometiniu Kauno inžinieriumi Antanu Jokimu vos per kelis mėnesius parengė naujojo rajono planą, kurį subraižė pagal Europoje itin populiarią miesto-sodo koncepciją.<br><br>

                    Kvartalo vystymo kryptį padiktavo ir pati gamta: mišria žaluma apaugę Žaliakalnio šlaitai bei didžiausias Europoje mieste esantis ąžuolynas. Statūs šlaitai apsunkino gatvių ir takų tiesimą, todėl ieškota kitokių jungčių tarp Žaliakalnio ir Naujamiesčio. Viena jų tapo 1931 metais įrengtas elektrinis funikulierius, o vėliau apatinę miesto terasą su žaliuoju kalnu sujungė dabartiniai Žaliakalnio simboliai – Kauko laiptai, „Aušros“ ir Fryko takai.<br><br>

                    Kuriant Žaliakalnį prioritetu tapo natūralaus miško išsaugojimas, o rajono veidu – maži sklypai su privačiais gyvenamaisiais namais ir sodais. Urbanistai pastatus apsupo žaluma, formavo pusapvales gatves, jas įrėmino medžių alėjomis. Miesto-sodo idėja buvo pagrįsta tuo, kad naujas gyvenimas kuriamas ne „sugrūstame“ daugiaaukščių mieste, bet gamtoje. Tokiu būdu naujakuriai turėjo visus miesto teikiamus privalumus bei sėmėsi sveikatos iš žalumos.
