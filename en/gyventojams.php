            <summary>How is a site listed?</summary>
            <div>
                Before a site can be considered for inclusion in the World Heritage List, it must undergo a lengthy nomination process that takes at least two years. Only the country in which the heritage site is located can submit the nomination proposal. Prior to starting the nomination process, the site must first be included in the Tentative List of the Member States' Heritage Sites of the Convention.<br><br>
                The country is required to prepare an application that, based on the selection criteria set by the World Heritage Committee, explains the reasons for nominating the site and provides protection and management measures, along with a detailed plan. This application is submitted by official government authorities. <br><br>
                Once the application is prepared, it is sent to the World Heritage Centre, where it is reviewed and verified. Approved documents are then forwarded to advisory organizations: The International Union for Conservation of Nature (IUCN) and the International Council on Monuments and Sites (ICOMOS). These organisations evaluate the proposal and submit a report to the World Heritage Committee. <br><br>
                The latter makes the final decision on whether to include the site in the World Heritage List.
