                    <span>The relocation of the Lithuanian Government to Kaunas in 1919 ushered in a transformative era. With the state's administration settling in, Naujamiestis rapidly evolved and transformed. As the city’s population surged, the demand for housing and workspace grew, leading to the reconstruction of Tsarist-era buildings, often with additional floors added.<br><br>

                    The construction boom peaked between 1930 and 1940, fuelled by the fading hope of reclaiming Vilnius. In this period, 3 to 6-story buildings emerged on the streets of the former Tsarist city, representing the new capital. Museums, banks, embassies, schools, and cultural and administrative institutions filled the growing district. Meanwhile, in the Žaliakalnis slopes, a district of residential homes and villas took shape, adding further to the city's urban fabric.<br><br>

                    Multifunctional buildings became the lifeblood of Kaunas, addressing the city's core needs: employment, commerce, education, healthcare, entertainment, and comfortable living.
