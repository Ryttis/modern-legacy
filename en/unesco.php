                        </div>
                    </div>
                    <span>In the interwar period, Žaliakalnis was the second-fastest growing district in the city, only behind Naujamiestis. Kaunas' burgomaster Jonas Vileišis brought in renowned urbanist Marius Frandsen from Copenhagen to help shape the district's future. Along with local engineer Antanas Jokimas, they devised a plan within months, following the popular European city-garden concept.<br><br>

                    Nature itself dictated the district's development: the lush greenery of the Žaliakalnis slopes and the largest oak grove in Europe, nestled within the city. The steep terrain presented construction challenges, prompting the search for alternative routes between Žaliakalnis and Naujamiestis. In 1931, the electric funicular was completed, symbolising Žaliakalnis. Later, the lower city terrace was connected to green mountain by the current symbols of Žaliakalnis – via the iconic Kaukas Steps and Aušra and Frykas Paths. <br><br>

                    When designing Žaliakalnis, preserving the natural forest was a top priority, shaping the district into a serene residential haven. Private homes with gardens dotted small plots, nestled among greenery, with semi-circular streets framed by tree-lined avenues. The city-garden concept championed the idea that new life was blossoming not in the cramped high-rises of the city, but in nature. This design gave residents the best of both worlds: urban convenience and the health benefits of abundant greenery.
                    </span>
                </div>
