        <h3>CRITERION IV</h3>
        <span>
            Modernist Kaunas was included in the UNESCO World Heritage List under Criterion IV. As an exceptional example of a historic city centre that experienced rapid urbanisation and modernisation while serving as the temporary capital (1919–1939), Modernist Kaunas embodies the values and aspirations of local residents who sought to create a modern city. Driven by post-war optimism and a belief in an independent future, it stands as a testament to early 20th-century Europe during a period of shifting national borders.<br><br>

            Through civic initiatives, the gradual development of Kaunas, respecting both the urban context and the natural environment, resulted in an exceptional cityscape and a distinct architectural language. This development not only met the needs of a growing population but also reflected the modernization of urban life in the 20th century. It is a testament to the community's optimism and creativity, even in the face of challenging political and economic conditions.
        </span>
